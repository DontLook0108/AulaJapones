
public class Aluno { 
	private String nome; 
	private int quantFaltas; 
	private double priMedia; 
	private double segMedia;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getQuantFaltas() {
		return quantFaltas;
	}
	public void setQuantFaltas(int quantFaltas) {
		this.quantFaltas = quantFaltas;
	}
	public double getPriMedia() {
		return priMedia;
	}
	public void setPriMedia(double priMedia) {
		this.priMedia = priMedia;
	}
	public double getSegMedia() {
		return segMedia;
	}
	public void setSegMedia(double segMedia) {
		this.segMedia = segMedia;
	}
	  
	
} 

